package com.example.javaproject2.week2.day4;

public class BitOperatorEx {
    public static void main(String[] args) {
        int iVal1 = 1;
        System.out.println(iVal1 << 1);  // 곱하기 계산
        System.out.println(iVal1 << 2);
        System.out.println(iVal1 << 3);

        int iVal2 = 32;
        System.out.println(iVal2 >> 1);  // 나누기 계산
        System.out.println(iVal2 >> 2);
    }
}
